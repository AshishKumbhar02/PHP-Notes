What is Variable Scope?
- variable scope refers to the context in which a variable can be accessed or modified.  
---------------------------------------------------------------------------------------------
--------------------------------------------------------------------------------------------- 
Types of Variable Scope:
1. Global Scope: Defined outside of functions, accessible anywhere in the script.
2. Local Scope: Defined inside a function, only accessible within that function.
3. Static Scope: A variable inside a function that keeps its value between function calls.
