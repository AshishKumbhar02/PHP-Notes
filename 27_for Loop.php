<?php

# Print numbers 1 to 5.
for ($i = 1; $i <= 5; $i++) {
    echo $i . "<br>";
}
echo "<hr>";


# Print Decrement numbers 10 to 1.
for ($i = 10; $i >= 1; $i--) {
    echo $i . "<br>";
}
echo "<hr>";


?>